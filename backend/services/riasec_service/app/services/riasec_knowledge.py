def build_group_definition_block() -> str:
    lines = []

    for group, profile in RIASEC_GROUP_PROFILES.items():
        lines.append(
            f"- {group} - {profile['name']} ({profile['career_label']}): "
            f"{profile['description']}"
        )

    return "\n".join(lines)


def build_scoring_indicator_block() -> str:
    lines = []

    for group, profile in RIASEC_GROUP_PROFILES.items():
        indicators = "; ".join(profile["assessment_indicators"])
        lines.append(f"- {group}: {indicators}.")

    return "\n".join(lines)


def build_question_cue_block(groups: list[str] | None = None) -> str:
    selected_groups = groups or list(RIASEC_GROUP_PROFILES)
    lines = []

    for group in selected_groups:
        profile = RIASEC_GROUP_PROFILES.get(group)

        if not profile:
            continue

        cues = "; ".join(profile["question_cues"])
        lines.append(f"- {group}: {cues}.")

    return "\n".join(lines)


def unique_items_for_code(field_name: str, riasec_code: str) -> list[str]:
    items = []

    for group in riasec_code:
        items.extend(RIASEC_GROUP_PROFILES.get(group, {}).get(field_name, []))

    return list(dict.fromkeys(items))
